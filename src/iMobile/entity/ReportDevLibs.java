package iMobile.entity;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@SuppressWarnings("serial")
@Entity
@Table(name = "ReportDevTestRes")
public class ReportDevLibs implements Serializable
{	
	@Id
	@Column(name = "id")
	private  Long id;
	
	@Column(name = "lib_name")
	private String libName;
	
	@Column(name = "lib_test_url")
	private String libTestUrl;
	
	@Column(name = "lib_prod_url")
	private String libProdUrl;
	
	@Column(name = "lib_desc")
	private String libDesc;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getLibName() {
		return libName;
	}

	public void setLibName(String libName) {
		this.libName = libName;
	}

	public String getLibTestUrl() {
		return libTestUrl;
	}

	public void setLibTestUrl(String libTestUrl) {
		this.libTestUrl = libTestUrl;
	}

	public String getLibProdUrl() {
		return libProdUrl;
	}

	public void setLibProdUrl(String libProdUrl) {
		this.libProdUrl = libProdUrl;
	}

	public String getLibDesc() {
		return libDesc;
	}

	public void setLibDesc(String libDesc) {
		this.libDesc = libDesc;
	}

	
	
	
}
